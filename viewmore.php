            <h5>How to Get There</h5>

            <p>
            When taking flights to the Philippines, the main jump-off point when going to Legazpi, Albay, or Bicol region is Metro Manila in Luzon. In Visayas, you can also get to Legazpi from Cebu and Leyte. Here are some of your options on how to get there: <br>
            </p>
            <p>
            By Flights<br>
Traveling via a Manila to Bicol flight or a Manila to Legazpi flight is the fastest and most convenient way to get to Albay City. Travel time for flights to Legazpi and flights to Bicol usually takes about 1 hour and 20 minutes. There are daily roundtrip flights to Manila from Bicol as well. You will land at Bicol International Airport (DRP). <br>
<br>By Bus <br>
If you have the luxury of time and would rather experience the trip via land, you can take a bus from Manila to Legazpi. The journey lasts for 9 hours. Make sure to book in advance via the go-to bus lines like Cagsawa, DLTB, and Penafrancia.<br><br>By Ferry
<br>
For visitors traveling from Leyte or Cebu, you can take the 12-15 hour trip through WG&A and Trans Asia shipping lines – both departing from Masbate Port to Legazpi, Albay.
